    private void logTransportBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logTransportBtnActionPerformed
        Object spinnerValue = distanceSpinner.getValue();
    
        if (spinnerValue == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a valid distance!", "Input Error", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
    
        try {
            double distance = ((Number) spinnerValue).doubleValue(); 
            if (distance <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Distance must be greater than zero!", "Input Error", javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }

            String vehicleType = cmbVehicleType.getSelectedItem().toString();
            // Calculation matching image_4.png baseline specs (e.g., Petrol Car = 0.170)
            double carbonEmitted = distance * 0.170; 

            // Match columns seen in image_5.png: user_id, category, details, activity_usage, calculated_co2e, log_date
            String sql = "INSERT INTO emission_logs (user_id, category, details, activity_usage, calculated_co2e, log_date) VALUES (?, 'Transport', ?, ?, ?, NOW())";
        
            try (Connection conn = DBConnection.getConnection();
                PreparedStatement pstmt = conn.prepareStatement(sql)) {
            
                int activeUser = (this.currentSessionUserId > 0) ? this.currentSessionUserId : 1; 
            
                pstmt.setInt(1, activeUser);
                pstmt.setString(2, vehicleType);
                pstmt.setDouble(3, distance);
                pstmt.setDouble(4, carbonEmitted);
            
                int rowsInserted = pstmt.executeUpdate();
                if (rowsInserted > 0) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Transport activity logged successfully!", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    distanceSpinner.setValue(0); // Clear spinner entry
    
                    // REFRESH TABLE: Forces table to pull the current user's updated database data rows
                    populateHistoryLogTable(activeUser); // Refresh history
                }
            }
            } catch (SQLException e) {
                e.printStackTrace();
                javax.swing.JOptionPane.showMessageDialog(this, "Database Write Error: " + e.getMessage(), "Database Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_logTransportBtnActionPerformed

    private void btnLogElectricityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogElectricityActionPerformed
        Object spinnerValue = electricitySpinner.getValue();
    
        if (spinnerValue == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a valid consumption value!", "Input Error", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
    
        try {
            double kwh = ((Number) spinnerValue).doubleValue(); 
            if (kwh <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Electricity consumption must be greater than zero!", "Input Error", javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }
        
            // Calculation factor: approx 0.45 kg CO2e per kWh
            double carbonEmitted = kwh * 0.45; 
        
            // Match columns seen in image_5.png: user_id, category, details, activity_usage, calculated_co2e, log_date
            String sql = "INSERT INTO emission_logs (user_id, category, details, activity_usage, calculated_co2e, log_date) VALUES (?, 'Utilities', 'Electricity', ?, ?, NOW())";
        
            try (Connection conn = DBConnection.getConnection();
                PreparedStatement pstmt = conn.prepareStatement(sql)) {
            
                int activeUser = (this.currentSessionUserId > 0) ? this.currentSessionUserId : 1;
            
                pstmt.setInt(1, activeUser);
                pstmt.setDouble(2, kwh);
                pstmt.setDouble(3, carbonEmitted);
            
                int rowsInserted = pstmt.executeUpdate();
                if (rowsInserted > 0) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Electricity log saved successfully!", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    electricitySpinner.setValue(0); // Clear spinner entry
    
                    // REFRESH TABLE: Synchronizes the shared history view table with new entries
                    populateHistoryLogTable(activeUser); // Refresh history
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "SQL Write Error: " + e.getMessage(), "Database Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLogElectricityActionPerformed

