    public void populateHistoryLogTable(int userId) {
        // 1. Set up headers matching your interface layout
        java.util.Vector<String> columns = new java.util.Vector<>(java.util.Arrays.asList(
            "Activity Type", "Details / Vehicle", "Usage Value", "Carbon Footprint (kg CO2e)", "Log Date"
        ));
        java.util.Vector<java.util.Vector<Object>> data = new java.util.Vector<>();

        // 2. Query filtered by the logged-in user's ID to keep entries isolated per user account
        String sql = "SELECT category, details, activity_usage, calculated_co2e, log_date FROM emission_logs " +
                 "WHERE user_id = ? ORDER BY log_date DESC";

        try (Connection conn = DBConnection.getConnection();
            PreparedStatement pstmt = conn.prepareStatement(sql)) {
        
            pstmt.setInt(1, userId);
        
            try (ResultSet rs = pstmt.executeQuery()) {
                while (rs.next()) {
                    java.util.Vector<Object> row = new java.util.Vector<>();
                    row.add(rs.getString("category"));          // 'Transport' or 'Utilities'
                    row.add(rs.getString("details"));           // e.g., 'Car (Petrol)' or 'Electricity'
                    row.add(rs.getDouble("activity_usage"));     // Distance value or kWh value
                    row.add(rs.getBigDecimal("calculated_co2e"));// Footprint mass matching database float
                    row.add(rs.getTimestamp("log_date"));        // Record creation date timestamp
                    data.add(row);
                }
            }
        
            // 3. Directly update your shared variable table model view
            tblHistoryLog.setModel(new javax.swing.table.DefaultTableModel(data, columns));
        
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

